#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int	len;
	int	i;

	if (!s || !f)
		return ;
	i = 0;
	len = ft_strlen(s);
	while (i < len)
		f(&s[i++]);
}
