#include "libft.h"

int	ft_count_num(long n)
{
	long			i;
	long			neg;

	i = 0;
	if (n < 0)
	{
		neg = -n;
		i++;
	}
	else
		neg = n;
	while (neg > 0)
	{
		neg /= 10;
		i++;
	}
	return (i);
}
