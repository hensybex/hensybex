#include "libft.h"

void    ft_putnbr_8(long n)
{
    if (n < 0)
    {
       ft_putchar('-');
        n = n * -1;
    }
    if (n < 8)
        ft_putchar('0' + n);
    else
    {
        ft_putnbr_8(n / 8);
        n = n % 8;
        ft_putchar('0' + n);
    }
}